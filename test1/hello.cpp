#include <iostream>

using namespace std;

 

int main()

{
    int num = 3;

    switch (num)

{

    case 1:

        cout << "1" << endl;

        break;

    case 2:

        cout << "2" << endl;

        break;

    case 3:

        cout << "3" << endl;

        break;

    case 4:

        cout << "4" << endl;

        break;

    case 5:

        cout << "5" << endl;

        break;

    default:

        cout << "type a number from 1 to 5" << endl;

        break;

}

}